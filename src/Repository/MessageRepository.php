 * @method Message|null find($id, $lockMode = null, $lockVersion = null)
 * @method Message|null findOneBy(array $criteria, array $orderBy = null)
 * @method Message[]    findAll()
 * @method Message[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class MessageRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Message::class);
    }
    /**
     * Récupère les messages entre un utilisateur et son employé.
     *
     * @param User $user L'utilisateur
     * @param User $employee L'employé
     * @return Message[] Les messages trouvés
     */
    public function findConversation(User $user, User $employee): array
    {
        return $this->createQueryBuilder('m')
            ->where('m.sender = :user AND m.recipient = :employee')
            ->orWhere('m.sender = :employee AND m.recipient = :user')
            ->setParameter('user', $user)
            ->setParameter('employee', $employee)
            ->orderBy('m.createdAt', 'ASC')
            ->getQuery()
            ->getResult();
    }

